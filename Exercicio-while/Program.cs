namespace Exercicio_while
{
    internal class Program
    {
        static void Main(string[] args)
        {
            // Faça um programa, utilizando while, que mostre na tela os números de 0 a 100.

            int meuContador = 1;

            while (meuContador <= 100)
                {
                    Console.WriteLine(meuContador);
                    meuContador++;
                } 
        }
    }
}